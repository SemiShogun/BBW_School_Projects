public class Model {
    private double chf;
    private double euro;
    private double pfund;
    private double HKD;

    public Model() {
    }

    public double getChf() {
        return chf;
    }

    public void setChf(double chf) {
        this.chf = chf;
    }

    public double getEuro() {
        return euro;
    }

    public void setEuro(double euro) {
        this.euro = euro;
    }

    public double getPfund() {
        return pfund;
    }

    public void setPfund(double pfund) {
        this.pfund = pfund;
    }

    public double getHKD() {
        return HKD;
    }

    public void setHKD(double HKD) {
        this.HKD = HKD;
    }

}
